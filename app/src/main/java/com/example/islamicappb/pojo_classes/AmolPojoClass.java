package com.example.islamicappb.pojo_classes;

public class AmolPojoClass {

    String AmolNumber;
    String Amol;
    String AmolSource;

    public AmolPojoClass(String amolNumber, String amol, String amolSource) {
        AmolNumber = amolNumber;
        Amol = amol;
        AmolSource = amolSource;
    }

    public String getAmolNumber() {
        return AmolNumber;
    }

    public String getAmol() {
        return Amol;
    }

    public String getAmolSource() {
        return AmolSource;
    }
}




